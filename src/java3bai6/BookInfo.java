    public void dodulieu() throws SQLException {
        model.setRowCount(0); //Xóa sạch dữ liệu trong bảng
        String sql = "Select * from Books";
        st = con.createStatement();
        rs = st.executeQuery(sql);

        if (rs.isBeforeFirst() == false) {
            JOptionPane.showMessageDialog(this, "Chưa có dữ liệu");
            return;
        }

        //Đổ dữ liệu
        Vector data;
        while (rs.next()) {
            data = new Vector();
            data.add(rs.getInt("id")); //Lấy ra cột id
            data.add(rs.getString("title"));//Lấy title
            data.add(rs.getFloat("price"));//Lấy price
            model.addRow(data);
        }
    }

