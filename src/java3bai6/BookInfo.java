    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        int vitri = tblData.getSelectedRow(); //lấy vị trí cần xóa
        if (vitri == -1) {
            JOptionPane.showMessageDialog(this, "Chưa chọn vị trí");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa hay không?") == 0) {

            try {

                int id = Integer.parseInt(tblData.getValueAt(vitri, 0).toString());
                String sql = "Delete from Books where id =" + id;
                st = con.createStatement();
                int kq = st.executeUpdate(sql);
                if (kq != -1) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    dodulieu();
                }

            } catch (SQLException ex) {
                System.out.println(ex);
            }

        }

    }//GEN-LAST:event_btnXoaActionPerformed

