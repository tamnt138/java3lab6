    public void timkiem()
    {
        String timten = txtTim.getText();
        if (timten.isEmpty()) //Không có dữ liệu
        {
            try {
                dodulieu();
            } catch (SQLException ex) {
                //   Logger.getLogger(BookInfo.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println(ex);
            }
        } else { //Ô tìm kiếm mới  có  dữ  liệu

            try {
                String sql = "select *  from Books where title like N'%" + timten + "%'";
                st = con.createStatement();
                rs = st.executeQuery(sql);
                model.setRowCount(0);  //Xóa tất cả  dữ liệu trong bảng
                
                if (rs.isBeforeFirst() == false) {
                  //  JOptionPane.showMessageDialog(this, "Không có sách vừa tìm");
                } else {
                
                    //Đổ dữ liệu
                    Vector data;
                    while (rs.next()) {
                        data = new Vector();
                        data.add(rs.getInt("id")); //Lấy ra cột id
                        data.add(rs.getString("title"));//Lấy title
                        data.add(rs.getFloat("price"));//Lấy price
                        model.addRow(data);
                    }

                }

            } catch (SQLException ex) {
                //   Logger.getLogger(BookInfo.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println(ex);
            }

        }
    }
    
    
