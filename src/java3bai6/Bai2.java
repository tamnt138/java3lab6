    public void capnhat() {
        String name = txtName.getText();
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        String parentName = txtParentName.getText();
        String standard = cboFees.getSelectedItem().toString();
        int vitri = tblData.getSelectedRow();
        int Id = Integer.parseInt(tblData.getValueAt(vitri, 0).toString()); //id nằm ở cột đầu tiên
        try {
            String sql = "update Students set Name=?, Address=?, ParentName=?,Phone=?, standard=? where Regid =?";
            PreparedStatement pr = con.prepareStatement(sql);
            pr.setString(1, name);
            pr.setString(2, address);
            pr.setString(3, parentName);
            pr.setString(4, phone);
            pr.setString(5, standard);
            pr.setInt(6, Id);
            pr.executeUpdate();
            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
            dodulieu();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi " + e);
        }

    }

