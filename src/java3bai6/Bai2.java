    public void hienthi(int vitri) throws SQLException {
        int ID = Integer.parseInt(tblData.getValueAt(vitri, 0).toString());
        //JOptionPane.showMessageDialog(this, ID + "");
        String sql = "Select a.* , b.Standid, b.Name StandardName\n"
                + "from Students a , Standard b\n"
                + "where a.standard = b.Standid and Regid =" + ID;
        st = con.createStatement();
        rs = st.executeQuery(sql);

        if (rs.isBeforeFirst() == false) {
            JOptionPane.showMessageDialog(this, "Chưa có dữ liệu");
            return;
        }

        //Đổ dữ liệu
        while (rs.next()) {
            txtName.setText(rs.getString(2));
            txtAddress.setText(rs.getString(3));
            txtParentName.setText(rs.getString(4));
            txtPhone.setText(rs.getString(5));
            cboStandard.setSelectedItem(rs.getString("StandardName"));
            cboFees.setSelectedItem(rs.getString("Standid"));
        }
    }

