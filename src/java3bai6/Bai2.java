    public void them() {
        String name = txtName.getText();
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        String parentName = txtParentName.getText();
        String standard = cboFees.getSelectedItem().toString();
        SimpleDateFormat time = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String date = time.format(new Date()); //Lấy ra ngày hiện tại
        try {
            String sql = "insert into Students\n"
                    + "values\n"
                    + "(?,?,?,?,?,?)";
            PreparedStatement pr = con.prepareStatement(sql);
            pr.setString(1, name);
            pr.setString(2, address);
            pr.setString(3, parentName);
            pr.setString(4, phone);
            pr.setString(5, standard);
            pr.setString(6, date);
            pr.executeUpdate();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            dodulieu();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }


